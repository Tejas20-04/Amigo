const mongoose = require("mongoose");
const { number } = require("motion");

const userSchema = new mongoose.Schema(
  {
    username: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    email: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    password: {
      type: String,
      required: true,
      minlength: 6,
    },
  },
  { timestamps: true },
); // adds createdAt and updatedAt automatically

module.exports = mongoose.model("User", userSchema);
